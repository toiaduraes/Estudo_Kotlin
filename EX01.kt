fun main() {
	val numero1 = 10
	val numero2 = 20
	val nome1 = "Carlos"
	val nome2 = "Carlos"
    
	// Comparação de números
	println(numero1 == numero2) //false, porque 10 não é igual a 10 - falso
	// Comparação de strings
	println(nome1 == nome2) //true, porque "Carlos" é igual a "Carlos" - verdadeiro
    
}
